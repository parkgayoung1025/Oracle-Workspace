CREATE TABLE BOARD(
  BOARD_NO NUMBER PRIMARY KEY,
  BOARD_TITLE VARCHAR2(100) NOT NULL,
  BOARD_CONTENT VARCHAR2(4000) NOT NULL,
  BOARD_CD CHAR(1) REFERENCES BOARD_TYPE,
  BOARD_WRITER NUMBER,
  COUNT NUMBER DEFAULT 0,
  CREATE_DATE DATE DEFAULT SYSDATE,
  STATUS VARCHAR2(1) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
  FOREIGN KEY (BOARD_WRITER) REFERENCES MEMBER
);

CREATE SEQUENCE SEQ_BNO NOCACHE;


CREATE TABLE BOARD_IMG (	
BOARD_IMG_NO NUMBER, 
ORIGIN_NAME VARCHAR2(200 BYTE), 
CHANGE_NAME VARCHAR2(200 BYTE), 
REF_BNO VARCHAR2(200 BYTE), 
IMG_LEVEL NUMBER(1,0), 
PRIMARY KEY ("BOARD_IMG_NO")
);

CREATE SEQUENCE SEQ_IMG_NO NOCACHE;

