SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS 
JOIN TB_CLASS_PROFESSOR USING(CLASS_NO) -- 과목 번호
JOIN TB_PROFESSOR P USING(PROFESSOR_NO) -- 교수 번호
JOIN TB_DEPARTMENT D ON (P.DEPARTMENT_NO = D.DEPARTMENT_NO)
WHERE D.CATEGORY = '인문사회'
ORDER BY TB_CLASS.DEPARTMENT_NO, 2 ASC; 

SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS 
JOIN TB_CLASS_PROFESSOR USING(CLASS_NO) -- 과목 번호
JOIN TB_PROFESSOR P USING(PROFESSOR_NO) -- 교수 번호
WHERE P.DEPARTMENT_NO IN (
    SELECT DEPARTMENT_NO FROM TB_DEPARTMENT
    WHERE CATEGORY = '인문사회')
ORDER BY TB_CLASS.DEPARTMENT_NO, 2 ASC;

