SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS 
JOIN TB_CLASS_PROFESSOR USING(CLASS_NO) -- 과목 번호
JOIN TB_PROFESSOR USING(PROFESSOR_NO) -- 교수 번호
ORDER BY TB_CLASS.DEPARTMENT_NO, 2 ASC; 

SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS C
JOIN TB_DEPARTMENT D ON (C.DEPARTMENT_NO = D.DEPARTMENT_NO)
JOIN TB_CLASS_PROFESSOR CP ON (CP.CLASS_NO = C.CLASS_NO)
JOIN TB_PROFESSOR P ON (P.PROFESSOR_NO = CP.PROFESSOR_NO)
ORDER BY DEPARTMENT_NO, 2 ASC;

